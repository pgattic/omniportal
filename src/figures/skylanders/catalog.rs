use crate::domain::{FigureKind, GameLine};

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum SkylandersElement {
    Air,
    Earth,
    Fire,
    Water,
    Magic,
    Tech,
    Life,
    Undead,
    Light,
    Dark,
    Unknown,
}

impl SkylandersElement {
    pub const fn wire_name(self) -> &'static str {
        match self {
            Self::Air => "air",
            Self::Earth => "earth",
            Self::Fire => "fire",
            Self::Water => "water",
            Self::Magic => "magic",
            Self::Tech => "tech",
            Self::Life => "life",
            Self::Undead => "undead",
            Self::Light => "light",
            Self::Dark => "dark",
            Self::Unknown => "unknown",
        }
    }

    pub const fn label(self) -> &'static str {
        match self {
            Self::Air => "Air",
            Self::Earth => "Earth",
            Self::Fire => "Fire",
            Self::Water => "Water",
            Self::Magic => "Magic",
            Self::Tech => "Tech",
            Self::Life => "Life",
            Self::Undead => "Undead",
            Self::Light => "Light",
            Self::Dark => "Dark",
            Self::Unknown => "Unknown",
        }
    }
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct FigureCatalogEntry {
    pub index: u16,
    pub game_line: GameLine,
    pub kind: FigureKind,
    pub series: &'static str,
    pub name: &'static str,
    pub character_id: u32,
    pub variant_id: u32,
}

impl FigureCatalogEntry {
    pub const fn has_variant(self) -> bool {
        self.variant_id != 0
    }

    pub const fn element(self) -> SkylandersElement {
        skylanders_element_for_character_id(self.character_id)
    }
}

